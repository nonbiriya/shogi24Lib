#検索結果格納クラス
class memberSearchResult:
    def getUserName(self):
        return self._userName
    def setUserName(self,userName):
        self._userName = userName

    def getUserId(self):
        return self._userId
    def setUserId(self,Id):
        self._userId = Id

    def getDanKyu(self):
        return self._dankyu
    def setDankyu(self,dankyu):
        self._dankyu = dankyu

    def getR(self):
        return self._R
    def setR(self,R):
        self._R = R
    
    def getWinRate(self):
        return self._winRate
    def setWinRate(self,winRate):
        self._winRate = winRate
    
    def getWin(self):
        return self._win
    def setWin(self,win):
        self._win = win

    def getLose(self):
        return self._lose
    def setLose(self,lose):
        self._lose = lose
    
    def getDrow(self):
        return self._drow
    def setDrow(self,drow):
        self._drow = drow

    def getBattle(self):
        return self._battle
    def setBattle(self,battle):
        self._battle = battle
    
    def getMaxR(self):
        return self._maxR
    def setMaxR(self,maxR):
        self._maxR = maxR
    
    def getResion(self):
        return self._resion
    def setResion(self,resion):
        self._resion = resion
    
    def getProf(self):
        return self._prof
    def setProf(self,prof):
        self._prof = prof
